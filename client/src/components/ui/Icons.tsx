// Centralized icons for consistency and DRY code

interface IconProps {
  className?: string;
  size?: 'sm' | 'md' | 'lg';
}

const sizeClasses = {
  sm: 'icon-sm',
  md: 'icon-md',
  lg: 'icon-lg'
};

function getClass(size: 'sm' | 'md' | 'lg' = 'md', className?: string) {
  return `${sizeClasses[size]} ${className ?? ''}`.trim();
}

